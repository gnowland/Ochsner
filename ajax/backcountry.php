		    <h1>Backcountry.com CSBS</h1>
		    <h4>Customer Service Bulletin Semi-Weekly digital archive project</h4>
		    <div class="timeline">Project Timeline: January 2013 &mdash; March 2013</div>
            </div>

            <div class="cell head mission cfull">
            <h2>the mission</h2>
            <p>Shortly after joining backcountry.com I recognized that there was an absence of adequate distribution and archiving of the internal company newsletter, the Customer Service Bulletin Semi-Weekly (CSBS). I initiated and led a project to digitalize and present a fully formatted and search-engine-optimized version of the newsletter on the internal company Wiki network.</p>
            </div>
            <div class="cell divider cfull" style="margin-top:1%">&nbsp;</div>
            <div class="cell head strategy cleft">
    		<h2>the strategy</h2>
    		<p>I really appreciated the way that Lexi (the newsletter founder) formatted the printed newsletter, succinct and clear for maximum information impact, and I wanted to translate that onto the wiki page. The Customer Service web team suggested that I just display a screenshot, but I wanted the text to be searchable, editable, and clear, so I knew that would not work.</p>
            </div>
           
            <div class="cell head dilemma cright">
    		<h2>the hurdle</h2>
    		<p>Before undertaking this endeavor I had only minimal experience with Wiki Markup Language (WML), so staying true to the original designâ€™s form was harder than expected considering the limitations of WML. Many times I had to code with no preview because the tabular formatting would break the window until it all came together. </p>
            </div>
            <div class="cell divider cfull break2" style="margin-top:1%;">&nbsp;</div>
            <div class="cell head execution cleft">
		    <h2>the execution</h2>
		    <p>I created an attractive gallery archive page with pictures of each issue that linked to the full text version. After a few iterations, I succeeded at creating a pixel-for-pixel replica of the original printed newsletter so customer service representatives could easily find important information about new products and techniques.</p>
           </div>
           
           <div class="cell head result cright">
            <h2>the result</h2>
		    <p>The customer service department supervisors were extremely impressed with my initiative to go above and beyond my job responsibilities, while also maintaining consistent sales quotas, and I received considerable recognition from upper management. I was brought in on PHP-based database statistics projects and was asked to participate in user interface/user experience discussions. The archive is still maintained using my original techniques.</p>
