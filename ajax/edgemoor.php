        	<div class="title">
		    <h1>Edgemoor Community Center</h1>
		    <h4>A non-profit community center providing daycare and after school care services</h4>
		    <div class="timeline">Project Timeline: September 2009 &mdash; June 2010</div>
            </div>

            <div class="cell head mission cleft">
            <h2>the mission</h2>
    		<p>I designed and developed Edgemoor Community Center’s website as part of a team project my senior year of college. Our mission was to produce a website that was not only informative and aesthetically pleasing but also employed cutting-edge usability practices and designs for handicapped and less-technologically-adept individuals.</p>
            </div>
            
            <div class="cell head strategy cright">
    		<h2>the strategy</h2>
    		<p>Because this was a childcare facility and community center I wanted to create a site that was simple in design yet visually stimulating. Edgemoor Community Center provided my team with a large amount of information to categorize, so creating a clear navigational hierarchy was key.</p>
            </div>
            <div class="cell divider cfull" style="margin-top:1%">&nbsp;</div>
            <div class="cell head dilemma cleft">
    		<h2>the hurdle</h2>
    		<p>Although I had never created a Flash animation before, I decided that it would be the best application for Edgemoor’s site. A simple animated header would certainly achieve the balance between “fun” and “simple” without overwhelming users who were on older systems or were not familiar with browsing the internet, as the header would not have an effect on navigation and would depreciate to a static image on slower systems.</p>
            </div>
            
            <div class="cell head execution cright">
		    <h2>the execution</h2>
		    <p>After performing numerous usability studies my team settled on a simple and traditional page layout with an animated header, left-hand navigation, and center body. We wanted to use primary colors, with minimal shading or gradients. Most importantly, we utilized Google apps integration to allow the community center employees to easily update the website after we finished the project.  </p>
           </div>
           <div class="cell divider cfull break2" style="margin-top:1%;">&nbsp;</div>
           <div class="cell head result cfull">
            <h2>the result</h2>
		    <p>Not only did my team receive an “A” on the project, but the community center hired me on as a contractor after the semester ended. I helped teach the staff how to update the website and they are still using my design.</p>
           </div>
