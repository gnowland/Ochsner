        	<div class="title">
		    <h1>Light Up The Queen Foundation</h1>
		    <h4>A non-profit theatre redevelopment and revitalization project in Wilmington, DE</h4>
		    <div class="timeline">Project Timeline: February 2009 &mdash; April 2009</div>
            </div>

            <div class="cell head mission cleft">
            <h2>the mission</h2>
    		<p>I was lead developer for the Light Up The Queen Foundation website as part of a team project my junior year of college. The Queen, a 45,000 square foot theatre in downtown Wilmington Delaware had been abandoned for five decades and the Light Up The Queen Foundation needed a website to inspire community interest in its redevelopment.</p>
            </div>
            
            <div class="cell head strategy cright">
    		<h2>the strategy</h2>
    		<p>One of the most striking features of the Queen Theatre was the state in which it had been left. Ancient movie projectors decayed with their last film reels still in place. The decorative wallpaper, once a glimmering red and gold, was peeling from the walls. Rays of light peaked through the ceiling, simultaneously illuminating the structure’s ornate details while exposing its disintegration. We yearned to capture the potential concealed under years of rust and deterioration.</p>
            </div>
            <div class="cell divider cfull" style="margin-top:1%">&nbsp;</div>
            <div class="cell head dilemma cleft">
    		<h2>the hurdle</h2>
    		<p>This was the first time that I worked exclusively on the development side of a project, which presented me with its own unique set of complications. Collaborating with the design team, film crew, research team and marketing teams meant that concessions would have to be made in order to incorporate all of their desired features while maintaining a functional and cohesive website.</p>
            </div>
            
            <div class="cell head execution cright">
		    <h2>the execution</h2>
		    <p>We developed a website heavily reliant on large, beautiful images to convey the elegance of the building. The Homepage featured a rotating set of images in the background. We incorporated a photo timeline and picture gallery, as well as a short film about the history of the theatre told through the memories of Wilmington residents who had been patrons of the theatre in their youth. </p>
           </div>
           <div class="cell divider cfull break2" style="margin-top:1%;">&nbsp;</div>
           <div class="cell head result cfull">
            <h2>the result</h2>
		    <p>The website was an important resource advocating for the redevelopment of the old theatre. WXPN radio station in Philadelphia announced that they would open a branch of their popular music dinner theatre, the World Café Live, in the queen. Wilmington real estate developers Buccini/Pollin Group and city officials soon joined in the initiative to restore the Queen Theater. In October 2009, six months after the launch of the website, the $25 million required for restoration was secured and the Light Up The Queen Foundation announced a Spring 2011 opening. The theatre remains a bastion of hope in the downtown Wilmington revitalization movement.</p>
           </div>
