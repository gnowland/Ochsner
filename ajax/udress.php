		    <h1>UDress Magazine</h1>
		    <h4>A student-run bi-yearly fashion publication at the University of Delaware</h4>
		    <div class="timeline">Project Timeline: April 2008 &mdash; May 2010</div>
            </div>

            <div class="cell head mission cfull">
            <h2>the mission</h2>
    		<h3>UDressmag.com</h3>
            <p>UDress Magazine recruited me as their Web Chairman after my website for DelRide SnowCorp started gaining attention in the University community. UDress Magazine was interested in building a similar online community for their members, creating a public forum where participants could discuss fashion together and share their favorite trends. They hoped to not only showcase the physical magazine, but also compliment it with editorial articles and exclusive online content. </p>
			<h3>lookbook.udressmag.com</h3>
			<p>A second website that I launched for UDress was an exclusive “lookbook” site, where members could post, comment, and rate each other’s outfits (similar to the popular fashion social network lookbook.nu). This website was limited to acting members of the magazine in order to help protect its users, and employed very sophisticated integration to the Facebook API. Unfortunately due to fundamental changes in the Facebook API the website is no longer accessible. Nonetheless, it was an extremely rewarding project to develop.</p>
            </div>
            <div class="cell divider cfull" style="margin-top:1%">&nbsp;</div>
            <div class="cell head strategy cleft">
    		<h2>the strategy</h2>
    		<p>I worked closely with the UDress executive team to understand what features they were looking for in an online community website and how they wanted to manage the relationship between the physical magazine and the online entity. Especially important to them was the ability to keep readers interested in the magazine between bi-yearly issues by providing a constant supply of new content to the site. I was provided with a web team to help write content and manage administration of the site.</p>
            </div>
           
            <div class="cell head dilemma cright">
    		<h2>the hurdle</h2>
    		<p>Because of my experience with DelRide I had an understanding of what would work for UDress. However, since this was going to be collaborative website, as opposed to running the entire site myself, I had to take into account ease of use in the backend. Because of this I decided to scrap Drupal and develop UDress’s website on the Joomla framework, which has a clear administration interface.</p>
            </div>
            <div class="cell divider cfull break2" style="margin-top:1%;">&nbsp;</div>
            <div class="cell head execution cleft">
		    <h2>the execution</h2>
		    <p>I led the web team in the installation and customization of the Joomla configuration, modifying the PHP code of nodes and modules in order to generate the preferred web application functions. I digitalized the magazine for online reading, adapting a Flash plug-in to animate digital page turns. I performed search engine optimization diagnostics and implemented controls to boost search rankings. I structured the backend for automated content creation, designing templates and assisting content editors with creating content for the website.</p>
           </div>
           
           <div class="cell head result cright">
            <h2>the result</h2>
		    <p>The new UDress website was very well received. Not only was the website popular among University students, it began attracting a world audience. Just one year after its launch the website was generating an average of over 4,000 page views per month, with traffic coming from more than 130 countries on 6 continents. The framework that I put into place made it so easy to publish new content that editors from other departments began posting their own sections on the website, which has now grown to almost 1,500 pages.</p>
           </div>
